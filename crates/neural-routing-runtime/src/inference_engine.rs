#[cfg(test)]
mod tests {
    use super::*;
    use neural_routing_policy::CodebookBuilder;

    fn make_embedding(seed: f32) -> Vec<f32> {
        (0..QUERY_DIM)
            .map(|i| ((i as f32 + seed) * 0.1).sin())
            .collect()
    }

    fn make_codebook() -> ActionCodebook {
        let mut builder = CodebookBuilder::new();
        let actions = [
            ("note", "create", 1.0),
            ("note", "search_semantic", 2.0),
            ("task", "update", 3.0),
            ("code", "search", 4.0),
            ("code", "analyze_impact", 5.0),
            ("plan", "create", 6.0),
            ("step", "update", 7.0),
        ];

        for (i, (tool, action, seed)) in actions.iter().enumerate() {
            let emb = make_embedding(*seed * 50.0);
            for _ in 0..10 {
                builder.observe(tool, action, &emb, 0.5 + i as f32 * 0.1);
            }
        }

        let mut codebook = builder.build();
        codebook.ood_threshold = 0.5;
        codebook
    }

    #[test]
    fn test_new_engine() {
        let engine = InferenceEngine::new(InferenceEngineConfig::default());
        assert!(!engine.is_ready());
        assert_eq!(engine.rtg_target(), 0.8);
    }

    #[test]
    fn test_infer_without_model_returns_fallback() {
        let engine = InferenceEngine::new(InferenceEngineConfig::default());
        let query = make_embedding(1.0);

        let result = engine.infer(&query, None, None).unwrap();
        assert_eq!(result.source, InferenceSource::Fallback);
        assert!(result.actions.is_empty());
    }

    #[test]
    fn test_infer_invalid_query_dim() {
        let engine = InferenceEngine::new(InferenceEngineConfig::default());
        let query = vec![1.0f32; 10]; // Wrong dimension

        let result = engine.infer(&query, None, None);
        assert!(result.is_err());
        match result {
            Err(InferenceError::InvalidInput(_)) => {}
            other => panic!("Expected InvalidInput, got {:?}", other),
        }
    }

    #[test]
    fn test_infer_no_codebook() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 4,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        // No codebook set

        let query = make_embedding(1.0);
        let result = engine.infer(&query, None, None);
        assert!(result.is_err());
        match result {
            Err(InferenceError::NoCodebook) => {}
            other => panic!("Expected NoCodebook, got {:?}", other),
        }
    }

    #[test]
    fn test_infer_with_random_model() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 4,
            rtg_target: 0.8,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        engine.set_codebook(make_codebook());

        assert!(engine.is_ready());

        let query = make_embedding(1.0);
        let result = engine.infer(&query, None, None).unwrap();

        assert_eq!(result.source, InferenceSource::PolicyNet);
        assert!(!result.actions.is_empty());
        assert!(result.actions.len() <= 4);
        assert_eq!(result.rtg_target, 0.8);
        assert!(result.latency_us > 0);

        // Each action should have valid fields
        for (i, action) in result.actions.iter().enumerate() {
            assert!(!action.tool.is_empty());
            assert!(!action.action.is_empty());
            assert_eq!(action.step, i);
            assert!(action.confidence > 0.0);
        }
    }

    #[test]
    fn test_infer_with_graph_embedding() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 3,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        engine.set_codebook(make_codebook());

        let query = make_embedding(1.0);
        let graph_emb = make_embedding(42.0);

        let result = engine.infer(&query, Some(&graph_emb), None).unwrap();
        assert_eq!(result.source, InferenceSource::PolicyNet);
        assert!(!result.actions.is_empty());
    }

    #[test]
    fn test_infer_with_tool_filter() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 4,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        engine.set_codebook(make_codebook());

        let query = make_embedding(1.0);
        let tools = vec!["code.search".to_string(), "note.create".to_string()];

        let result = engine.infer(&query, None, Some(&tools)).unwrap();
        assert_eq!(result.source, InferenceSource::PolicyNet);

        // All decoded actions should be from the filter set (or OOD)
        for action in &result.actions {
            let key = format!("{}.{}", action.tool, action.action);
            assert!(
                tools.contains(&key) || action.is_ood,
                "Action {} should be in filter or OOD",
                key
            );
        }
    }

    #[test]
    fn test_set_rtg_target() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig::default());
        assert_eq!(engine.rtg_target(), 0.8);

        engine.set_rtg_target(0.95);
        assert_eq!(engine.rtg_target(), 0.95);
    }

    #[test]
    fn test_infer_latency_reasonable() {
        // With a random model and small max_steps, inference should be fast
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 4,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        engine.set_codebook(make_codebook());

        let query = make_embedding(1.0);

        // Warm up
        let _ = engine.infer(&query, None, None);

        // Measure
        let start = Instant::now();
        let iterations = 5;
        for _ in 0..iterations {
            let _ = engine.infer(&query, None, None);
        }
        let avg_us = start.elapsed().as_micros() / iterations as u128;

        eprintln!("InferenceEngine avg latency: {}μs ({} steps)", avg_us, 4);

        // In debug mode, allow generous budget. Release should be <15ms.
        assert!(
            avg_us < 5_000_000, // 5 seconds in debug
            "Average latency {}μs is too high",
            avg_us
        );
    }

    #[test]
    fn test_different_rtg_produces_different_routes() {
        let mut engine = InferenceEngine::new(InferenceEngineConfig {
            max_steps: 4,
            ..Default::default()
        });
        engine.load_random_model().unwrap();
        engine.set_codebook(make_codebook());

        let query = make_embedding(1.0);

        engine.set_rtg_target(0.1);
        let result_low = engine.infer(&query, None, None).unwrap();

        engine.set_rtg_target(0.99);
        let result_high = engine.infer(&query, None, None).unwrap();

        // With different RTG targets, at least confidence values should differ
        // (actions may or may not differ depending on random weights)
        let conf_low: f32 = result_low.actions.iter().map(|a| a.confidence).sum();
        let conf_high: f32 = result_high.actions.iter().map(|a| a.confidence).sum();

        // They should produce some result
        assert!(!result_low.actions.is_empty());
        assert!(!result_high.actions.is_empty());

        eprintln!(
            "RTG 0.1 → {} actions (conf sum {:.3}), RTG 0.99 → {} actions (conf sum {:.3})",
            result_low.actions.len(),
            conf_low,
            result_high.actions.len(),
            conf_high
        );
    }
}
